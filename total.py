total=0
for num in range (101):
    total = total + num
print("The total is", total)

## This code calculates the sum of all numbers from 0 to 100 using a for loop.
# It initializes a variable `total` to 0, then iterates through each number in the range from 0 to 100,
# adding each number to `total`. Finally, it prints the total sum.                                            